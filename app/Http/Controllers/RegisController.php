    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        
        $validator = Validator::make($request->all(),[
            'username' => 'required|min:6|string',
            'email' => 'required',
            'password' =>  ['required','string',Password::min(6)->numbers()]
        ]);

        if($validator->fails() ){
            return redirect('/register')->withFail("Username length must be at least 6 characters.")->with('fail1',"Password must at least 8 characters and alpha numeric.");
        }else{
        $user = new User();
        $user->username = $request->username;
        $user->email = $request->email;
        $user->password = Hash::make($request->password);
        $user->save();
        return redirect('/register')->with('message',"Your Account has been Created");
        }
        
        
    }

